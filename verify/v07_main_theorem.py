def ind_contains(g, h):
    """g O^3 contains h O^3."""
    return min(ind_vpi(x) for row in (g.inv() * h).m for x in row) >= 0


def ind_index_log3(g, h):
    q = g.inv() * h
    assert min(ind_vpi(x) for row in q.m for x in row) >= 0
    return ind_vpi(q.det())


