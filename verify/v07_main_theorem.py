def ind_v3(n):
    """3-adic valuation of a nonzero integer (independent)."""
    n = abs(int(n))
    assert n != 0
    v = 0
    while n % 3 == 0:
        n //= 3
        v += 1
    return v


def ind_vpi(x: Fw):
    """Independent v_pi: v_chi(num) = v_3(N(num)) since N(chi)=3 and the
    chi-free part has norm coprime to 3; v_pi(1/den) = -2*v_3(den)."""
    if x.is_zero():
        return inf
    return ind_v3(x.num.norm()) - 2 * ind_v3(x.den) if x.den % 3 == 0 \
        else ind_v3(x.num.norm())


